const normalizeTitle = (value = "") =>
  String(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, " ")
    .trim();

export function getVisualResonanceProfile(track = {}) {
  const sortOrder = Number(track.sort_order ?? track.track_number ?? track.trackNumber);
  const title = normalizeTitle(track.title || track.name);

  return visualResonanceTracks.find((profile) => {
    if (Number.isFinite(sortOrder) && profile.trackNumber === sortOrder) return true;
    return title && normalizeTitle(profile.title) === title;
  }) || null;
}

export function enrichTrackWithVisualResonance(track = {}) {
  const profile = getVisualResonanceProfile(track);

  if (!profile) return track;

  return {
    ...track,
    core_theme: track.core_theme || profile.coreTheme,
    signal_types: track.signal_types || profile.signalTypes,
    lyric_mode: track.lyric_mode || profile.lyricMode,
    visual_skin: track.visual_skin || profile.visualSkin,
    motion_style: track.motion_style || profile.motionStyle,
    particle_types: track.particle_types || profile.particles,
    resonance_events: track.resonance_events || profile.events,
    visual_resonance_note: track.visual_resonance_note || profile.sourceNote,
    visual_resonance: profile,
  };
}
